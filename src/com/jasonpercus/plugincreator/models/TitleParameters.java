package com.jasonpercus.plugincreator.models;



/**
 * 
 * @author JasonPercus
 * @version 1.0
 */
public class TitleParameters {
    
    
    
    /**
     * Corresponds to the title font name
     */
    public String fontFamily;
    
    /**
     * Corresponds to the title font size
     */
    public int fontSize;
    
    /**
     * Corresponds to the title font style (bold, italic...)
     */
    public String fontStyle;
    
    /**
     * Determines whether the title is underlined or not
     */
    public boolean fontUnderline;
    
    /**
     * Determines whether the title is showed or not
     */
    public boolean showTitle;
    
    /**
     * Corresponds to the title position
     */
    public String titleAlignment;
    
    /**
     * Corresponds to the color of the title
     */
    public String titleColor;
    
    
    
}